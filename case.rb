weather = 'hot'

case weather
when 'rainy'
  puts "Put on your boots and bring an umbrella"
when 'sunny'
  puts "Leave your jacket at home and put on sunscreen. Don't forget your sunglasses"
when 'snowy'
  puts "Wear a thick winter jacket and put on your snowboots"
when 'hot'
  puts "Take the day off and go to the beach"
end
